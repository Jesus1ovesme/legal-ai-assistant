/**
 * Поиск в локальной таблице по запросу. Простой fuzzy: lower-case query
 * matches alias substring or shortName/title. Возвращает топ N matches.
 */
export function searchLegalCorpus(query: string, limit = 10): LegalAct[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  const scored = LEGAL_CORPUS.map((act) => {
    let score = 0;
    if (act.shortName.toLowerCase() === q) score += 100;
    if (act.title.toLowerCase().includes(q)) score += 50;
    if (act.shortName.toLowerCase().includes(q)) score += 40;
    for (const alias of act.aliases) {
      if (alias === q) score += 60;
      else if (alias.includes(q)) score += 30;
      else if (q.includes(alias)) score += 20;
    }
    return { act, score };
  });

  return scored
    .filter((x) => x.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((x) => x.act);
}

export function consultantUrl(id: string, articleId?: string): string {
  if (articleId) return `https://www.consultant.ru/document/cons_doc_LAW_${id}/${articleId}/`;
  return `https://www.consultant.ru/document/cons_doc_LAW_${id}/`;
}

/**
 * URL поиска по ГАРАНТу для cross-verification. Возвращает search-страницу
 * (SPA, результаты подгружаются JS), но кликабельную для юриста.
 * Claude может попробовать WebFetch на эту страницу — если результаты в HTML
 * — извлечёт. Если только UI-shell — даст ссылку юристу для ручной проверки.
 */
export function garantSearchUrl(query: string): string {
  return `https://www.garant.ru/search/?source=base&query=${encodeURIComponent(query)}`;
}

/** URL поиска на pravo.gov.ru/search для свежих актов. */
export function pravoSearchUrl(query: string): string {
  return `https://publication.pravo.gov.ru/search?text=${encodeURIComponent(query)}`;
}

export interface CitationParse {
  raw: string;
  article: number | null;
  paragraph: number | null;
  lawHint: string | null;
  matched: LegalAct | null;
}

/**
 * Парсит цитату вида "ст. 152 ГК РФ", "пункт 2 статьи 1064 ГК", "ч.1 ст. 14 ФЗ-40".
 * Возвращает структуру + best-match из corpus.
 */
export function parseCitation(raw: string): CitationParse {
  const text = raw.trim();
  // ст. N или статья N или ст N
  const articleMatch = text.match(/(?:ст(?:\.|атья)?\s*|статьи\s+)(\d{1,4})/i);
  const article = articleMatch?.[1] ? Number(articleMatch[1]) : null;
  // пункт N или п. N или ч. N или часть N
  const paraMatch = text.match(/(?:п(?:\.|ункт)?\s*|ч(?:\.|асть)?\s*)(\d{1,3})/i);
  const paragraph = paraMatch?.[1] ? Number(paraMatch[1]) : null;
  // Удаляем «ст. 152» и пр. чтобы остался lawHint
  const lawHint = text
    .replace(/(?:ст(?:\.|атья)?|статьи)\s*\d+/gi, "")
    .replace(/(?:п(?:\.|ункт)?|пункта|ч(?:\.|асть)?|части)\s*\d+/gi, "")
    .replace(/[№#]\s*\d+/g, "")
    .trim();
  const matched = lawHint ? searchLegalCorpus(lawHint, 1)[0] ?? null : null;

  return { raw: text, article, paragraph, lawHint: lawHint || null, matched };
}
